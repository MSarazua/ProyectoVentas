    private void CargarProductos(){
        
        DefaultTableModel modeloTabla =  (DefaultTableModel)tbInventario.getModel();
        modeloTabla.setRowCount(0); 
        
        ResultSet rs;  
        ResultSetMetaData rsmd; 
        int Columnas;
        
        
        try{
                    
            clsQuerys objBD = new clsQuerys();
            rs = objBD.fncConsultaInventario(0);
            rsmd = rs.getMetaData();
            Columnas = rsmd.getColumnCount();
            
            while(rs.next()){
                Object[] fila = new Object[Columnas];
                
                for(int indice = 0; indice < Columnas; indice ++){
                    
                    fila[indice] = rs.getObject(indice + 1 );
                }
                modeloTabla.addRow(fila);
                
            }
                    
                    
        }catch(SQLException e){
            System.out.println(e.toString());
        }
        
        
    }
    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
      if(fncValidaFormulario()==1){
             clsProducto objProducto = new clsProducto();

             
            
             
            objProducto.setNombre(  ctNombre.getText()   );
            objProducto.setDescripcion(  ctDescripcion.getText()   );
             objProducto.setCantidad( Integer.valueOf(ctCantidad.getText()));
              if (Integer.valueOf(ctCantidad.getText()) < 1 ) {
              ctEstado.setText("N");
                }else{
              ctEstado.setText("S");
              }
             objProducto.setPrecio( Double.valueOf( ctPrecio.getText())    );
              objProducto.setEstatus(  ctEstado.getText()   );
               objProducto.setCOD_PRODUTO( Integer.valueOf( ctCOD_PRODUTO.getText() )  );
       
          
             clsQuerys objBD = new clsQuerys();  
                     
            if(  objBD.fncModificar(objProducto)==1){
                    JOptionPane.showMessageDialog(this, "Producto del inventario ACTUALIZADO.");
                    CargarProductos();
                    Limpiar();
                    
             }else{
                    JOptionPane.showMessageDialog(this, "Producto del inventario NO ACTUALIZADO");   
             }
            }

    }//GEN-LAST:event_btnActualizarActionPerformed

