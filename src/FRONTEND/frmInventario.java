     private int fncValidaFormulario(){
        int Resp =0;
        if( ctNombre.getText().equals("") ){
                  JOptionPane.showMessageDialog(this, "Debe ingresar el nombre del producto.");
              }else{

                  if(ctDescripcion.getText().equals("")){
                       JOptionPane.showMessageDialog(this, "Debe ingresar la descripcion del producto.");
                  }else{
                       if (ctCantidad.getText().equals("")){
                            JOptionPane.showMessageDialog(this, "Debe ingresar la cantidad del producto.");
                       }else{
                           if (ctPrecio.getText().equals("")){
                                JOptionPane.showMessageDialog(this, "Debe ingresar el precio del producto.");
                           }else{
                                Resp =1;
                           }
                       }        


                  }

                }
        
        return Resp;
    }

    
    
