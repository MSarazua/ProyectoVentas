    private void tbInventarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbInventarioMouseClicked
        try{
            int fila = tbInventario.getSelectedRow();
            int Codigo =   Integer.parseInt(  tbInventario.getValueAt(fila, 0).toString()    )  ;
            ResultSet rs;
            clsQuerys objBD = new clsQuerys();
            rs = objBD.fncConsultaInventario(Codigo);
            
            while(rs.next()){
                    ctNombre.setText( rs.getString("PRODUCTO")  );
                    ctDescripcion.setText( rs.getString("DESCRIPCION")  );
                    ctCantidad.setText( rs.getString("CANTIDAD")  );
                    ctPrecio.setText(    rs.getString("PRECIO")     );
                   ctEstado.setText(    rs.getString("ESTATUS")     );
                    ctCOD_PRODUTO.setText(  rs.getString("COD_PRODUTO")  );
                      
                   if ("S".equals(ctEstado.getText())) {
                    lbExistente.setVisible(true);
                    lbInexistente.setVisible(false);
                }else{
                   
                    lbExistente.setVisible(false);
                    lbInexistente.setVisible(true);              
                   }
              
            }
         
        }catch(Exception Ex){
            System.out.println( Ex.getMessage());
        }
    }//GEN-LAST:event_tbInventarioMouseClicked

